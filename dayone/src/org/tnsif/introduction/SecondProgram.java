package org.tnsif.introduction;

//Programs to demonstrate on various data types

public class SecondProgram {

	public static void main(String[] args) {
		// int gives us the whole number
		// float gives the decimal number

		int num1=5/2; 	
		//if we use f after any number float gives precision upto 7 decimal number
		float num2=103f/44f;	
		//if we use d after any number double gives the precision upto 16 decimal places
		double num3=10d/3d;
		System.out.println("V1: "+num1);
		System.out.println("V2: "+num2);
		System.out.println("V3: "+num3);
		
	}

}